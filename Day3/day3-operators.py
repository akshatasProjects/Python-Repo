# Declaring the variable at the top first
a = 3
b = 2

# Arithmetic operations and assigning the result to a variable
sum = a + b
diff = a - b
product = a * b
division = a / b
remainder = a % b
floor_division = a // b
expo = a ** b

print('a + b = ', sum)
print('a - b = ', diff)
print('a * b = ', product)
print('a / b = ', division)
print('a % b = ', remainder)
print('a // b = ', floor_division)
print('a ** b = ', expo)