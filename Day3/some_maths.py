# Let us start start connecting the dots and start making use of what 
# we already know to calculate (area, volume,density, weight, perimeter, distance, force)

# calculating Area of circle
radius = 10
area_of_circle = 3.142 * radius ** 2
print("Area of circle is :" ,area_of_circle)

# Calculating area of a rectangle
length = 10
width = 20
area_of_rect = length * width
print("Area of rectangle :", area_of_rect)

# Calculating a weight of an object
mass = 75
gravity = 9.81
weight = mass * gravity
print(weight, "N")

# Calculate the density of a liquid
mass = 75 # in Kg
volume = 0.075 # in cubic meter
density = mass / volume # 1000 Kg/m^3